import { createContext, useContext, useEffect, useState, ReactNode } from "react";

export type Language = "en" | "ar" | "ur" | "fa";

interface I18nContextType {
  t: (key: string) => string;
  language: Language;
  setLanguage: (lang: Language) => void;
  dir: "ltr" | "rtl";
}

const I18nContext = createContext<I18nContextType>({
  t: (key: string) => key,
  language: "en",
  setLanguage: () => {},
  dir: "ltr",
});

const rtlLanguages: Language[] = ["ar", "ur", "fa"];

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    if (typeof window !== "undefined") {
      return (localStorage.getItem("phx-lang") as Language) || "en";
    }
    return "en";
  });

  const dir = rtlLanguages.includes(language) ? "rtl" : "ltr";

  useEffect(() => {
    document.documentElement.dir = dir;
    document.documentElement.lang = language;
    localStorage.setItem("phx-lang", language);
  }, [language, dir]);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
  };

  const t = (key: string): string => {
    return translations[language]?.[key] || translations.en?.[key] || key;
  };

  return (
    <I18nContext.Provider value={{ t, language, setLanguage, dir }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useTranslation() {
  return useContext(I18nContext);
}

